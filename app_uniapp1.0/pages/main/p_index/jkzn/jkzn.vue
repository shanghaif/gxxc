			</view><view class="boby" style="text-indent: 50rpx;">（二）原机动车驾驶证因未提交身体条件证明被注销的；
			</view><view class="boby" style="text-indent: 50rpx;">（三）原机动车驾驶证由本人申请注销的；
			</view><view class="boby" style="text-indent: 50rpx;">（四）原机动车驾驶证因身体条件暂时不符合规定被注销的；
			</view><view class="boby" style="text-indent: 50rpx;">（五）原机动车驾驶证因其他原因被注销的，但机动车驾驶证被吊销或者被撤销的除外；
			</view><view class="boby" style="text-indent: 50rpx;">（六）持有的军队、武装警察部队机动车驾驶证超过有效期的；
			</view><view class="boby" style="text-indent: 50rpx;">（七）持有的境外机动车驾驶证超过有效期的。
