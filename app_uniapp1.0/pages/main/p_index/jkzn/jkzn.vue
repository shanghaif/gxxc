			</view><view class="boby" style="text-indent: 50rpx;">第十五条 已持有机动车驾驶证，申请增加准驾车型的，应当在本记分周期和申请前最近一个记分周期内没有记满12分记录。申请增加中型客车、牵引车、大型客车准驾车型的，还应当符合下列规定：
			</view><view class="boby" style="text-indent: 50rpx;">（一）申请增加中型客车准驾车型的，已取得驾驶城市公交车、大型货车、小型汽车、小型自动挡汽车、低速载货汽车或者三轮汽车准驾车型资格三年以上，并在申请前最近连续三个记分周期内没有记满12分记录；
			</view><view class="boby" style="text-indent: 50rpx;">（二）申请增加牵引车准驾车型的，已取得驾驶中型客车或者大型货车准驾车型资格三年以上，或者取得驾驶大型客车准驾车型资格一年以上，并在申请前最近连续三个记分周期内没有记满12分记录；
			</view><view class="boby" style="text-indent: 50rpx;">（三）申请增加大型客车准驾车型的，已取得驾驶城市公交车、中型客车或者大型货车准驾车型资格五年以上，或者取得驾驶牵引车准驾车型资格二年以上，并在申请前最近连续五个记分周期内没有记满12分记录。
			</view><view class="boby" style="text-indent: 50rpx;">正在接受全日制驾驶职业教育的学生，已在校取得驾驶小型汽车准驾车型资格，并在本记分周期和申请前最近一个记分周期内没有记满12分记录的，可以申请增加大型客车、牵引车准驾车型。
			</view><view class="boby" style="text-indent: 50rpx;">第十六条 有下列情形之一的，不得申请大型客车、牵引车、城市公交车、中型客车、大型货车准驾车型：
			</view><view class="boby" style="text-indent: 50rpx;">（一）发生交通事故造成人员死亡，承担同等以上责任的；
			</view><view class="boby" style="text-indent: 50rpx;">（二）醉酒后驾驶机动车的；
			</view><view class="boby" style="text-indent: 50rpx;">（三）被吊销或者撤销机动车驾驶证未满十年的。
			</view><view class="boby" style="text-indent: 50rpx;">第十七条 持有军队、武装警察部队机动车驾驶证，或者持有境外机动车驾驶证，符合本规定的申请条件，可以申请相应准驾车型的机动车驾驶证。
			</view><view class="boby" style="text-indent: 50rpx;">第十八条 申领机动车驾驶证的人，按照下列规定向车辆管理所提出申请：
			</view><view class="boby" style="text-indent: 50rpx;">（一）在户籍所在地居住的，应当在户籍所在地提出申请；
			</view><view class="boby" style="text-indent: 50rpx;">（二）在户籍所在地以外居住的，可以在居住地提出申请；
			</view><view class="boby" style="text-indent: 50rpx;">（三）现役军人（含武警），应当在居住地提出申请；
			</view><view class="boby" style="text-indent: 50rpx;">（四）境外人员，应当在居留地或者居住地提出申请；
			</view><view class="boby" style="text-indent: 50rpx;">（五）申请增加准驾车型的，应当在所持机动车驾驶证核发地提出申请；
			</view><view class="boby" style="text-indent: 50rpx;">（六）接受全日制驾驶职业教育，申请增加大型客车、牵引车准驾车型的，应当在接受教育地提出申请。
			</view><view class="boby" style="text-indent: 50rpx;">　第十九条 初次申请机动车驾驶证，应当填写申请表，并提交以下证明：
			</view><view class="boby" style="text-indent: 50rpx;">（一）申请人的身份证明；
			</view><view class="boby" style="text-indent: 50rpx;">（二）县级或者部队团级以上医疗机构出具的有关身体条件的证明。属于申请残疾人专用小型自动挡载客汽车的，应当提交经省级卫生主管部门指定的专门医疗机构出具的有关身体条件的证明。
			</view><view class="boby" style="text-indent: 50rpx;">第二十条 申请增加准驾车型的，应当填写申请表，提交第十九条规定的证明和所持机动车驾驶证。属于接受全日制驾驶职业教育，申请增加大型客车、牵引车准驾车型的，还应当提交学校出具的学籍证明。
			</view><view class="boby" style="text-indent: 50rpx;">第二十一条 持军队、武装警察部队机动车驾驶证的人申请机动车驾驶证，应当填写申请表，并提交以下证明、凭证：
			</view><view class="boby" style="text-indent: 50rpx;">（一）申请人的身份证明。属于复员、转业、退伍的人员，还应当提交军队、武装警察部队核发的复员、转业、退伍证明；
			</view><view class="boby" style="text-indent: 50rpx;">（二）县级或者部队团级以上医疗机构出具的有关身体条件的证明；
			</view><view class="boby" style="text-indent: 50rpx;">（三）军队、武装警察部队机动车驾驶证。
			</view><view class="boby" style="text-indent: 50rpx;">第二十二条 持境外机动车驾驶证的人申请机动车驾驶证，应当填写申请表，并提交以下证明、凭证：
			</view><view class="boby" style="text-indent: 50rpx;">（二）县级以上医疗机构出具的有关身体条件的证明。属于外国驻华使馆、领馆人员及国际组织驻华代表机构人员申请的，按照外交对等原则执行；
			</view><view class="boby" style="text-indent: 50rpx;">（三）所持机动车驾驶证。属于非中文表述的，还应当出具中文翻译文本。
			</view><view class="boby" style="text-indent: 50rpx;">申请人属于内地居民的，还应当提交申请人的护照或者《内地居民往来港澳通行证》、《大陆居民往来台湾通行证》。
			</view><view class="boby" style="text-indent: 50rpx;">第二十三条 实行小型汽车、小型自动挡汽车驾驶证自学直考的地方，申请人可以使用加装安全辅助装置的自备机动车，在具备安全驾驶经历等条件的人员随车指导下，按照公安机关交通管理部门指定的路线、时间学习驾驶技能，按照第十九条或者第二十条的规定申请相应准驾车型的驾驶证。
