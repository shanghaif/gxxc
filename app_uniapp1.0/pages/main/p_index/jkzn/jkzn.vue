			</view><view class="boby" style="text-indent: 50rpx;">
				6、下肢：双下肢健全且运动功能正常，不等长度不得大于5厘米。但左下肢缺失或者丧失运动功能的，可以申请小型自动挡汽车准驾车型的机动车驾驶证；
			</view><view class="boby" style="text-indent: 50rpx;">
