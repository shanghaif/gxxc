			</view><view class="boby" style="text-indent: 50rpx;">
				第十三条 有下列情形之一的，不得申请机动车驾驶证:
			</view><view class="boby" style="text-indent: 50rpx;">
				（一）有器质性心脏病、癫痫病、美尼尔氏症、眩晕症、癔病、震颤麻痹、精神病、痴呆以及影响肢体活动的神经系统疾病等妨碍安全驾驶疾病的；
			</view><view class="boby" style="text-indent: 50rpx;">
