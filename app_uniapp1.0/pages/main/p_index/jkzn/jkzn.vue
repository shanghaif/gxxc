			</view><view class="boby" style="text-indent: 50rpx;">第二十四条 申请机动车驾驶证的人，符合本规定要求的驾驶许可条件，具有下列情形之一的，可以按照第十四条第一款和第十九条的规定直接申请相应准驾车型的机动车驾驶证考试：
