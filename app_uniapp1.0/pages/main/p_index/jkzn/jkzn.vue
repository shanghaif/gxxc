			</view><view class="boby" style="text-indent: 50rpx;">（未取得机动车驾驶证驾驶机动车，有第一款第五项至第七项行为之一的，在规定期限内不得申请机动车驾驶证。
			</view><view class="boby" style="text-indent: 50rpx;">第十四条 初次申领机动车驾驶证的，可以申请准驾车型为城市公交车、大型货车、小型汽车、小型自动挡汽车、低速载货汽车、三轮汽车、残疾人专用小型自动挡载客汽车、普通三轮摩托车、普通二轮摩托车、轻便摩托车、轮式自行机械车、无轨电车、有轨电车的机动车驾驶证。
